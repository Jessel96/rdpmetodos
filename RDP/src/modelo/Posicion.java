package modelo;

/**
 *
 * @author marza
 */
public class Posicion {
    int x,y;

    public Posicion() {
    }

    public Posicion(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public String toString() {
        return super.toString();
    }
    
}
